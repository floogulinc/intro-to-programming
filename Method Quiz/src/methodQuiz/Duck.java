package methodQuiz;

public class Duck {

	int age;
	int size;
	
	public void duckQuack() {
		System.out.println("Quack Quack, I'm a duck and my age is " + age + " years, and I am " + size + " feet tall");
	}
	
	public void duckRun (int speed) {
		System.out.println("I run at a speed of " + speed + " mph");
	}
	
}
